	@Override
	@RequestMapping(value = "/entidad", method = RequestMethod.PUT)
	public @ResponseBody ResponseEntity<Entidad> save(@RequestBody(required = true) Entidad entidad) throws Exception {

		try {
			String userId = getHeader(HeaderValueNames.HEADER_USER_ID);
			if (!esSoloLectura(userId)) {
				log.debug("ENTIDAD A GUARDAR >> " + entidad);
				if (entidad.getId() == null) {

					mngrEntidad.save(entidad);
					return new ResponseEntity<Entidad>(entidad, HttpStatus.CREATED);
				} else {
					mngrEntidad.update(entidad);
					return new ResponseEntity<Entidad>(entidad, HttpStatus.OK);
				}
			} else {
				return new ResponseEntity<Entidad>(entidad, HttpStatus.BAD_REQUEST);
			}
		} catch (Exception e) {
			log.error(e.getLocalizedMessage());
			
			throw e;
		}
	}

